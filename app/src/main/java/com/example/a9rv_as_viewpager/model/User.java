package com.example.a9rv_as_viewpager.model;

public class User {
    public String abubakr;
    public String tohir;

    public User(String abubakr, String tohir) {
        this.abubakr = abubakr;
        this.tohir = tohir;
    }

    public String getAbubakr() {
        return abubakr;
    }

    public String getTohir() {
        return tohir;
    }
}
